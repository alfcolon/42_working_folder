void	ft_findobs(void) //record obstacle count and grid positions of osbtacles
{
	int	i;
	int 	j;

	i = 0;
	while (i < 47)
	{
		j = 0;
		while (j < 27)
		{
			if (grid[i][j])
			{
				obs[count][0] = i;
				obs[count][1] = j;
				if (obs_cols[j] == '0')
					obs_cols[j] = '1';
				printf("count:%d\t\t[%d,%d]\n", count, i, j);
				count++;
			}
			j++;
		}
		i++;
	}
}
